#include "libft.h"

int	ft_fact(int nb)
{
	if (nb >= 13 || nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	if (nb != 1)
	{
		nb--;
		return ((nb + 1) * ft_fact(nb));
	}
	return (nb);
}
